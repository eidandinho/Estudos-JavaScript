// &&  (AND) - Combina dois valores e retorna um true ou false. Ele só vai retornar true, quando os dois valores forem verdadeiros.

var x = 3;
var y = 7;

x === 3 && y === 7;
console.log(x === 3 && y === 7); //teste

x === 3 && y === 4; //
console.log(x === 3 && y === 4); //teste

// || (OR) - Um único valor verdadeiro.

x === 3 || y === 3; 
console.log(x === 3 || y === 3); //teste

// ! (NOT) - Inverte valores. Não muito usado, pode-se usar como x != 3;

! x === 3;
console.log(! x === 3); //teste

x != 3;
console.log(x != 3); //teste

x !== 3;
console.log(x != 3); //teste

x != '3'; //string - X é igual a 3 string
console.log(x != '3'); //teste

x !== '3'; //string
console.log(x !== '3'); //teste

x !== 3; //
console.log(x !== 3); //teste

